//! # Linnet
//!
//! Linnet is a Rust library dedicated to graph data structures and algorithms.
//! Its primary focus is on providing tools and functionalities for working with
//! sub-graphs and performing node-identification tasks.
//!
//! This library is useful for scenarios where you need to analyze or manipulate
//! parts of a graph, or identify and manage specific nodes within a larger
//! graph structure.

pub mod drawing;
pub mod half_edge;
pub mod num_traits;
pub mod parser;
pub mod permutation;
pub mod tree;
pub mod union_find;
